let tecla = 'a'

if (tecla === 'a') {
    console.log("Se mueve a la izquierda");
} else if (tecla === 'd') {
    console.log("Se mueve a la derecha");
} else if (tecla === 's') {
    console.log("Se mueve hacia atras");
} else if (tecla === 'w') {
    console.log("Se mueve hacia adelante");
} else {
    console.log("No hace nada");
}
